#OPERADORES DE COMPARACIÓN
print('Operadores de comparación')
#Operador      Comparacion
# ==           es igual que
# !=           es distinto de
# >            es mayor que
# <            es menor que
# >=           es mayor o igual que
# <=           es menor o igual que

igual_que = 5 == 6

distinto_de = 5 != 6

mayor_que = 5 > 6

menor_que = 5 < 6

mayor_o_igual = 5 >= 6

menor_o_igual = 5 <= 6

print(menor_o_igual)
print()

